void initMap() {
  for (int y = 0; y < GRID_COLS; y++) {
    for (int x = 0; x < GRID_ROWS; x++) {
      grid.tiles[y][x] = *rawMap[y][x];
    }
  }
}

char getTile(int x, int y) {
    if (x > GRID_ROWS &&  y > GRID_COLS)
    {
        return '$';
    }

    return grid.tiles[y][x];
}

void setTile(int x, int y, char c) {
    if (x > GRID_ROWS &&  y > GRID_COLS)
    {
        return;
    }

    grid.tiles[y][x] = c;
}

